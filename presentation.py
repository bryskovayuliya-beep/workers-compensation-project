import streamlit as st

st.title("Презентация проекта")

st.markdown("""
## Проект: Прогнозирование стоимости страховых выплат

### Цель проекта
Разработать модель машинного обучения для предсказания итоговой стоимости страхового возмещения 
на основе характеристик работника и параметров страхового случая.

### Описание датасета
Используется датасет Workers Compensation из OpenML.  
Он содержит 100000 записей о страховых случаях и набор признаков, связанных с работником, условиями труда и параметрами страхового случая.

### Основные этапы работы
1. Загрузка датасета из OpenML
2. Предобработка данных
3. Преобразование признаков даты и времени
4. Кодирование категориальных признаков
5. Разделение данных на обучающую и тестовую выборки
6. Обучение модели Random Forest Regressor
7. Оценка качества модели с помощью метрик
8. Анализ важности признаков

### Предобработка данных
В ходе работы были выполнены следующие шаги:
- datetime-признаки преобразованы в числовые характеристики;
- созданы новые признаки: месяц происшествия, день недели и задержка сообщения;
- категориальные признаки преобразованы в числовой формат;
- датасет уменьшен до 10000 записей для ускорения обучения модели.

### Использованная модель
В проекте использована модель RandomForestRegressor.  
Она хорошо подходит для задач регрессии, умеет выявлять нелинейные зависимости и позволяет оценить важность признаков.

### Метрики оценки
Для оценки качества модели использовались:
- MAE
- MSE
- RMSE
- R²

### Результат
Создано Streamlit-приложение с двумя страницами:
- страница анализа и обучения модели;
- страница презентации проекта.

### Вывод
Разработанное приложение демонстрирует полный цикл работы с задачей регрессии:
от загрузки и подготовки данных до обучения модели и анализа результатов.
""")